public class loop {
    public static void main(String[] args) {
        //While Loop

        int i = 0; // initialize the variable 'i' to 0
        while (i < 5) { // as long as 'i' is less
        // than 5, keep looping
        System.out.println("The value of 'i' is " + i); // print
        // out the current value of 'i'
        i++; // increment 'i' by one after each iteration
        }

        //
    }
    
}
