public class operators {
    public static void main(String[] args) {
        int x1 = 10;
        int x2 = 10;

        System.out.println("Value of x1 is " + x1); // concatenation operator used
        System.out.println("Value of x2 is " + x2); // concatenation operator used
    
        System.out.println(x1+x2); //addition operator used
        System.out.println(x1-x2); //subtraction operator used
        System.out.println(x1*x2); //multiplication operator used
        System.out.println(x1/x2); //division operator used
    
        // relational operator
        boolean result = x1 == x2;
        System.out.println(result);


    }
}
